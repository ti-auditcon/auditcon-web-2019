    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(CategoryRequest $request)
    {
      $exist = Category::where('category', $request->category)->get();
      if (!count($exist))
      {
        $category = new Category;
        $category->category = $request->category;
        if ($category->save())
        {
          Session::flash('success','La categoría ha sido creada exitosamente');
          return redirect('/blog/panel/categories');
        }
      }
      Session::flash('warning','La categoría ya existe');
      return redirect('/blog/panel/categories/new');
    }

